public class livre {

    /**
     * Default constructor
     */
   
    private String refer_uniq;
    private String titre;
    private String auteur;
    private String editeur;
    private String anne_pub;

    

   

    /**
     * 
     */
    public String getrefer_uniq() {
        return refer_uniq;
    }

    public void setrefer_uniq(String refer_uniq) {
        this.refer_uniq = refer_uniq;
    }

    /**
     * 
     */
    public String gettitre() {
        return titre;
    }

    public void settitre(String titre) {
        this.titre = titre;
    }

    /**
     * 
     */
    public String getauteur() {
        return auteur;
    }

    public void setauteur(String auteur) {
        this.auteur = auteur;
    }

    /**
     * 
     */
    public String getediteur() {
        return editeur;
    }

    public void setediteur(String editeur) {
        this.editeur = editeur;
    }

    /**
     * 
     */
    public String getanne_pub() {
        return anne_pub;
    }

    public void setanne_pub(String anne_pub) {
        this.anne_pub = anne_pub;
    }

}